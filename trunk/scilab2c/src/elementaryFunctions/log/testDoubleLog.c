void dlogsTest(void) {
  double value = 0;
  double maxValue = 1;
  double increment = 1e-3;
  printf(">> Double scalar\n");
  while (value <= maxValue)
    {
      printf("dlogs(%e) = %e\n", value, dlogs(value));
      assert(dlogs(value) == log(value));
      value += increment;
    }
}

